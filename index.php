<td><h1>QUICKAR: Automatic Query Reformulation for Concept Location using Crowdsourced Knowledge </h1></td>
</tr>

<tr>
<td colspan="2">
<p align="justify" class="rounded2" >
<strong>Abstract:</strong> During maintenance, software developers deal with numerous change requests made by the users of a software system.
Studies show that the developers find it challenging to select appropriate search terms from a change request during concept location.  
In this paper, we propose a novel technique--QUICKAR--that automatically suggests helpful reformulations for a given query by leveraging the crowdsourced knowledge from Stack Overflow.
It determines semantic similarity or relevance between any two terms by analyzing their adjacent word lists from the programming questions of Stack Overflow, and 
then suggests semantically relevant queries for concept location. Experiments using 510 queries from two software systems suggest that our technique
can improve or preserve the quality of 76% of the initial queries on average which is promising. Comparison with one baseline technique validates our preliminary findings, and also demonstrates the potential of our technique.
</p>
